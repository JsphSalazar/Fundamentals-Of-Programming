print ("The program will show you the factorial of a non-negative integer")
print ("Would you like to start?")
st = input("Yes/No: ")
while st == "yes" or st == "Yes":

    n = input("Please give me a number: ")
    n = int (n)
    factorial = 1
    if n < 0:
        print("Factorial does not exist for negative numbers")
    elif n == 0:
        print ("The factorial of 0 is 1")
    else:
        for x in range (1, n + 1):
            factorial = factorial * x
        print("The factorial of", n, "is", factorial)
    st = input("Would you like to continue? ")

print ("Thanks for use the program. Have a nice day!")
